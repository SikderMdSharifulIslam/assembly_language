INCLUDE 'EMU8086.INC'
.MODEL SMALL
.STACK 100H
.DATA
 F DW ?
 C DW ?
 
 result_msg db "Temperature in Celsius: $"
 
.CODE 
MAIN PROC 
 MOV AX, @DATA
 MOV DS, AX  
 
 PRINTN "ENTER TEMPARATURE IN CELSIUS : "


 
 ;INPUT
MOV F,0
MOV BL,10

INPUT:
MOV AH,1
INT 21H
CMP AL,13
JE NEXT
SUB AL,30H
MOV AH,0
MOV CX,AX
MOV AX,F
MUL BL
ADD AX,CX
MOV F,AX
JMP INPUT

NEXT:

MOV AX, F
MOV BX,32
SUB AX,BX
MOV BX,5
MUL BX
MOV BX,9
DIV BX
 
 
 MOV C,AX
  

PRINTN ''
PRINTN "TEMPARATURE IN farenheight: "



MOV AX, C
 CALL PrintNumber
 
 MOV AH, 4Ch
 INT 21h
MAIN ENDP
PrintNumber PROC
 MOV CX, 10 
 MOV BX, 0 
PRINT_LOOP:
 XOR DX, DX 
 DIV CX
 ADD DL, '0' 
 PUSH DX
INC BX 
 TEST AX, AX
JNZ PRINT_LOOP 
PRINT_BACK:
 POP DX 
 MOV AH, 02h 
 INT 21H
 DEC BX
 JNZ PRINT_BACK 
 RET
PrintNumber ENDP
END MAIN