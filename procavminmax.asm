

ORG 100h

.DATA
COUNT DW 5
INDEX DW ?
LARGEST DB 0
SMALLEST DB 10
ARRAY DB 10 DUP (0)
MSG1 DB 'Input 5 numbers: ', '$'
MSG2 DB 0Dh, 0Ah, 'AVERAGE = ', '$'
MSG3 DB 0Dh, 0Ah, 'LARGEST = ', '$'
MSG4 DB 0Dh, 0Ah, 'SMALLEST = ', '$'

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    LEA DX, MSG1
    MOV AH, 9
    INT 21h

    MOV CX, COUNT
    LEA SI, ARRAY
    MOV INDEX, SI

INPUT_1:
    MOV AH, 1
    INT 21h
    SUB AL, 48
    MOV [SI], AL
    INC SI
    MOV AH, 2
    MOV DX, ' '
    INT 21h
LOOP INPUT_1

    LEA DX, MSG2
    MOV AH, 9
    INT 21h

    CALL CALC_AVG
    MOV AH, 2
    MOV DX, AX
    ADD DX, 48
    INT 21h

    LEA DX, MSG3
    MOV AH, 9
    INT 21h

    CALL CALC_LARGEST
    MOV AH, 2
    MOV DL, LARGEST
    ADD DL, 48
    INT 21h

    LEA DX, MSG4
    MOV AH, 9
    INT 21h

    CALL CALC_SMALLEST
    MOV AH, 2
    MOV DL, SMALLEST
    ADD DL, 48
    INT 21h

MAIN ENDP

CALC_AVG PROC
    MOV CX, COUNT
    MOV SI, INDEX
    XOR AX, AX
    XOR BX, BX
    XOR DX, DX

LOOP_AVG:
    MOV BL, [SI]
    ADD AL, BL
    INC SI
LOOP LOOP_AVG

    MOV BX, 5
    DIV BX
    RET
CALC_AVG ENDP

CALC_LARGEST PROC
    MOV CX, COUNT
    MOV SI, INDEX
    XOR AX, AX

LOOP_LARGEST:
    MOV AL, [SI]
    CMP AL, LARGEST
    JLE LESSEQUAL
    MOV LARGEST, AL

LESSEQUAL:
    INC SI
LOOP LOOP_LARGEST
    RET
CALC_LARGEST ENDP

CALC_SMALLEST PROC
    MOV CX, COUNT
    MOV SI, INDEX
    XOR AX, AX

LOP_SMALLEST:
    MOV AL, [SI]
    CMP AL, SMALLEST
    JGE GREATEREQUAL
    MOV SMALLEST, AL

GREATEREQUAL:
    INC SI
LOOP LOP_SMALLEST
    RET
CALC_SMALLEST ENDP

END MAIN

ret
