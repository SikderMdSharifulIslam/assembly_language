

ORG 100H

.DATA
  N DW ?
  FACTORIAL DW ?

.CODE
MAIN PROC
  MOV AX, @DATA
  MOV DS, AX

  MOV AH, 1
  INT 21H

  MOV AH, 0
  SUB AL,30H
  MOV N, AX

  MOV AX, 1
  MOV CX, N

LFACTORIAL:
  CMP CX,0
  JE DONE
  MUL CX
  DEC CX
  JMP LFACTORIAL

DONE:
  MOV FACTORIAL, AX

  MOV AH, 4CH
  INT 21H
MAIN ENDP

END MAIN

RET
